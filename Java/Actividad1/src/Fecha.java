
public class Fecha {
	/**
     * Indica el día.
     */
	private int dia;
	
	/**
     * Indica el mes.
     */
	private int mes;
	
	/**
     * Indica el año.
     */
	private int anho;
	
	
	/**
     * Retorna el día.
     * @return el día.
     */
    public int darDia( )
    {
        return dia;
    }
    
    /**
     * Retorna el mes.
     * @return el mes.
     */
    public int darMes( )
    {
        return mes;
    }
    
    /**
     * Retorna el año.
     * @return el año.
     */
    public int darAnho( )
    {
        return anho;
    }

}
