
public class Fraccionario {
	/**
     * Numerador del fraccionario.
     */
	private int numerador;
	
	/**
     * Denominador del fraccionario.
     */
	private int denominador;
	
	
	/**
     * Retorna el numerador de la fracción.
     * @return numerador de la fraccion.
     */
    public int darNumerador( )
    {
        return numerador;
    }
    
    /**
     * Retorna el denominador de la fracción.
     * @return denominador de la fraccion.
     */
    public int darDenominador( )
    {
        return denominador;
    }
}
