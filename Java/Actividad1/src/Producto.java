
public class Producto {
	/**
     * Indica la velocidad inicial del proyectil.
     */
	private double velocidad;
	
	/**
     * Indica el angulo del proyectil.
     */
	private double angulo;
	
	
	
	/**
     * Retorna la velocidad inicial del proyectil.
     * @return la velocidad inicial del proyectil.
     */
    public double darVelocidad( )
    {
        return velocidad;
    }
    
    /**
     * Retorna el angulo del proyectil.
     * @return el angulo del proyectil.
     */
    public double darAngulo( )
    {
        return angulo;
    }

}
