
public class Reloj {
	/**
     * Indica la hora en el reloj.
     */
	private int horas;
	
	/**
     * Indica los minutos en el reloj.
     */
	private int minutos;
	
	/**
     * Indica los segundos en el reloj.
     */
	private int segundos;
	
	
	/**
     * Retorna la hora en el reloj.
     * @return la hora en el reloj.
     */
    public int darHoras( )
    {
        return horas;
    }
    
    /**
     * Retorna los minutos en el reloj.
     * @return los minutos en el reloj.
     */
    public int darMinutos( )
    {
        return minutos;
    }
    
    /**
     * Retorna los segundos en el reloj.
     * @return los segundos en el reloj.
     */
    public int darSegundos( )
    {
        return segundos;
    }

}
